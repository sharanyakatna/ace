@torch.no_grad()
def compute_confidence(model, loader, device, max_batches=10):
    model.eval()
    confidences = []

    for i, batch in enumerate(loader):
        if i >= max_batches:
            break
        x = batch[0].to(device)
        logits = model(x)
        probs = F.softmax(logits, dim=1)
        max_probs = probs.max(dim=1).values
        confidences.append(max_probs.mean().item())

    return float(np.mean(confidences))


def ace_freeze_ratio(confidence, prev_confidence, min_r, max_r, threshold):
    if prev_confidence is None:
        return min_r

    if confidence < prev_confidence * threshold:
        return min_r

    ratio = min_r + (max_r - min_r) * confidence
    return float(np.clip(ratio, min_r, max_r))


def apply_freeze(model, freeze_ratio):
    encoder = model[0]
    classifier = model[1]

    encoder_layers = list(encoder.children())
    n_freeze = int(len(encoder_layers) * freeze_ratio)

    frozen_names = []
    for i, layer in enumerate(encoder_layers):
        freeze = (i < n_freeze)
        for param in layer.parameters():
            param.requires_grad = not freeze
        if freeze:
            frozen_names.append(f"layer_{i}")

    for param in classifier.parameters():
        param.requires_grad = True

    n_trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)

    return frozen_names, n_trainable

