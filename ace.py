class DistillationLoss(nn.Module):
    def __init__(self, temperature=2.0, alpha=0.7):
        super().__init__()
        self.temperature = temperature
        self.alpha = alpha
        self.ce_loss = nn.CrossEntropyLoss()

    def forward(self, student_logits, teacher_logits, labels):
        ce = self.ce_loss(student_logits, labels)

        if teacher_logits is None:
            return ce

        soft_student = F.log_softmax(student_logits / self.temperature, dim=1)
        soft_teacher = F.softmax(teacher_logits / self.temperature, dim=1)

        kd = F.kl_div(soft_student, soft_teacher, reduction='batchmean')
        kd = kd * (self.temperature ** 2)

        return self.alpha * ce + (1 - self.alpha) * kd

class PrototypeMemory:
    def __init__(self, feature_dim=512):
        self.prototypes = {}
        self.class_counts = {}

    @torch.no_grad()
    def update(self, task_id, features, labels):
        features = features.detach().cpu()
        labels = labels.cpu()

        if task_id not in self.prototypes:
            self.prototypes[task_id] = {}
            self.class_counts[task_id] = {}

        for feat, label in zip(features, labels):
            label = label.item()

            if label not in self.prototypes[task_id]:
                self.prototypes[task_id][label] = feat.clone()
                self.class_counts[task_id][label] = 1
            else:
                count = self.class_counts[task_id][label]
                self.prototypes[task_id][label] = (self.prototypes[task_id][label] * count + feat) / (count + 1)
                self.class_counts[task_id][label] += 1

    def get_all_prototypes(self):
        all_protos = []
        for task_protos in self.prototypes.values():
            for proto in task_protos.values():
                all_protos.append(proto)

        if len(all_protos) == 0:
            return None

        return torch.stack(all_protos)

    def compute_separation_loss(self, current_features, device):
        all_protos = self.get_all_prototypes()

        if all_protos is None:
            return torch.tensor(0.0, device=device)

        all_protos = all_protos.to(device)

        batch_size = current_features.size(0)
        sample_indices = torch.randperm(batch_size)[:min(32, batch_size)]
        sampled_features = current_features[sample_indices]

        distances = torch.cdist(sampled_features, all_protos, p=2)

        margin = 2.5
        loss = F.relu(margin - distances).mean()

        return loss

@torch.no_grad()
def compute_confidence(model, loader, device, max_batches=10):
    model.eval()
    confidences = []
    for i, batch in enumerate(loader):
        if i >= max_batches:
            break
        x = batch[0].to(device)
        logits = model(x)
        probs = F.softmax(logits, dim=1)
        max_probs = probs.max(dim=1).values
        confidences.append(max_probs.mean().item())
    return float(np.mean(confidences))

def ace_freeze_ratio(confidence, prev_confidence, min_r, max_r, threshold):
    if prev_confidence is None:
        return min_r
    if confidence < prev_confidence * threshold:
        return min_r
    ratio = min_r + (max_r - min_r) * confidence
    return float(np.clip(ratio, min_r, max_r))

def apply_freeze(model, freeze_ratio):
    encoder = model[0]
    classifier = model[1]
    encoder_layers = list(encoder.children())
    n_freeze = int(len(encoder_layers) * freeze_ratio)
    frozen_names = []
    for i, layer in enumerate(encoder_layers):
        freeze = (i < n_freeze)
        for param in layer.parameters():
            param.requires_grad = not freeze
        if freeze:
            frozen_names.append(f"layer_{i}")
    for param in classifier.parameters():
        param.requires_grad = True
    n_trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    return frozen_names, n_trainable

def get_layerwise_optimizer(model, freeze_ratio, base_lr=1e-3):
    encoder = model[0]
    classifier = model[1]

    encoder_layers = list(encoder.children())
    n_freeze = int(len(encoder_layers) * freeze_ratio)

    param_groups = []

    for i, layer in enumerate(encoder_layers):
        if i < n_freeze:
            continue

        lr_mult = 1.0 - (i / len(encoder_layers)) * 0.5

        layer_params = [p for p in layer.parameters() if p.requires_grad]

        if len(layer_params) > 0:
            param_groups.append({
                'params': layer_params,
                'lr': base_lr * lr_mult
            })

    classifier_params = [p for p in classifier.parameters() if p.requires_grad]
    param_groups.append({
        'params': classifier_params,
        'lr': base_lr * 2.0
    })

    return torch.optim.Adam(param_groups, weight_decay=1e-4)

