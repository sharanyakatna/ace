def apply_freeze(model, freeze_ratio):
    encoder = model[0]
    classifier = model[1]

    blocks = ['conv1', 'bn1', 'layer1', 'layer2', 'layer3', 'layer4']
    modules = [(name, module) for name, module in encoder.named_children() if name in blocks]

    n_freeze = int(len(modules) * freeze_ratio)
    frozen = []

    for i, (name, module) in enumerate(modules):
        freeze = i < n_freeze
        for p in module.parameters():
            p.requires_grad = not freeze
        if freeze:
            frozen.append(name)

    for p in classifier.parameters():
        p.requires_grad = True

    n_trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    return frozen, n_trainable

