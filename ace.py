@torch.no_grad()
def compute_confidence(model, loader, device, max_batches=10):
    model.eval()
    confidences = []
    for i, batch in enumerate(loader):
        if i >= max_batches:
            break
        x = batch[0].to(device)
        logits = model(x)
        probs = F.softmax(logits, dim=1)
        max_probs = probs.max(dim=1).values
        confidences.append(max_probs.mean().item())
    return float(np.mean(confidences))


def ace_freeze_ratio(confidence, prev_confidence, task_id, min_r, max_r, threshold):
    if prev_confidence is None:
        return min_r

    conf_delta = confidence - prev_confidence

    if conf_delta < -(1 - threshold) * prev_confidence:
        return min_r

    task_progress = (task_id + 1) / 5.0
    confidence_factor = confidence

    ratio = min_r + (max_r - min_r) * (0.4 * confidence_factor + 0.6 * task_progress)

    return float(np.clip(ratio, min_r, max_r))


def apply_freeze(model, freeze_ratio):
    encoder = model[0]
    classifier = model[1]

    all_layers = list(encoder.children())
    n_freeze = int(len(all_layers) * freeze_ratio)

    frozen_names = []
    for i, layer in enumerate(all_layers):
        freeze = (i < n_freeze)
        for param in layer.parameters():
            param.requires_grad = not freeze
        if freeze:
            frozen_names.append(f"layer_{i}")

    for param in classifier.parameters():
        param.requires_grad = True

    n_trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    return frozen_names, n_trainable


def run_ace(seed):
    print(f"\n{'='*60}")
    print(f"ACE Pure - Seed {seed}")
    print(f"{'='*60}")

    torch.manual_seed(seed)
    np.random.seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)

    backbone = models.resnet18(weights=None)
    backbone.fc = nn.Identity()
    state_dict = torch.load(SSL_ENCODER_PATH, map_location=device)
    backbone.load_state_dict(state_dict)
    backbone.to(device)

    classifier = nn.Linear(512, 100)
    model = nn.Sequential(backbone, classifier).to(device)

    benchmark = SplitCIFAR100(n_experiences=5, seed=seed, return_task_id=False)

    results_ace = []
    prev_confidence = None

    eval_plugin = EvaluationPlugin(
        accuracy_metrics(epoch=True, experience=True, stream=True),
        forgetting_metrics(experience=True, stream=True),
        loggers=[]
    )

    for exp in benchmark.train_stream:
        task_id = exp.current_experience
        print(f"\nACE Seed {seed} | Task {task_id}")

        conf_loader = DataLoader(exp.dataset, batch_size=CONFIG['train_mb_size'], shuffle=True)
        confidence = compute_confidence(model, conf_loader, device, max_batches=CONFIG['confidence_batches'])

        freeze_ratio = ace_freeze_ratio(
            confidence, prev_confidence, task_id,
            CONFIG['ace_min_freeze'], CONFIG['ace_max_freeze'],
            CONFIG['confidence_threshold']
        )

        frozen_layers, n_trainable = apply_freeze(model, freeze_ratio)

        print(f"  Confidence: {confidence:.4f}, Freeze: {freeze_ratio:.2%}")

        optimizer = torch.optim.Adam(
            filter(lambda p: p.requires_grad, model.parameters()),
            lr=CONFIG['lr'],
            weight_decay=CONFIG['weight_decay']
        )

        strategy = Naive(
            model=model,
            optimizer=optimizer,
            criterion=nn.CrossEntropyLoss(),
            train_mb_size=CONFIG['train_mb_size'],
            train_epochs=CONFIG['train_epochs'],
            eval_mb_size=CONFIG['eval_mb_size'],
            device=device,
            evaluator=eval_plugin
        )

        strategy.train(exp)
        eval_results = strategy.eval(benchmark.test_stream)

        results_ace.append({
            'task': task_id,
            'confidence': confidence,
            'prev_confidence': prev_confidence,
            'freeze_ratio': freeze_ratio,
            'frozen_layers': frozen_layers,
            'n_trainable': n_trainable,
            'metrics': eval_results
        })

        prev_confidence = confidence

    with open(f"{RESULTS_DIR}/results_ace_pure_seed{seed}.json", "w") as f:
        json.dump(results_ace, f, indent=2)

    print(f"\nACE Seed {seed} complete")
    return results_ace


