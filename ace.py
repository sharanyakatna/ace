def extract_stream_accs(results):
    accs = []
    for r in results:
        metrics = r.get('metrics', r)
        for k, v in metrics.items():
            if "Top1_Acc_Stream" in k:
                accs.append(v)
                break
    return accs

def extract_per_exp_accs(results):
    per_exp = []
    for r in results:
        metrics = r.get('metrics', r)
        exp_accs = {}
        for k, v in metrics.items():
            if "Top1_Acc_Exp/eval_phase/test_stream" in k:
                parts = k.split("Exp")
                if len(parts) > 1:
                    exp_num = int(parts[-1][:3])
                    exp_accs[exp_num] = v
        per_exp.append(exp_accs)
    return per_exp

