class ReplayBuffer:
    def __init__(self, size_per_task=500):
        self.size_per_task = size_per_task
        self.buffer = {}

    def add_task(self, task_id, dataset):
        n_samples = len(dataset)
        if n_samples <= self.size_per_task:
            indices = list(range(n_samples))
        else:
            indices = np.random.choice(n_samples, self.size_per_task, replace=False)

        self.buffer[task_id] = Subset(dataset, indices)

    def get_all_data(self):
        if len(self.buffer) == 0:
            return None
        datasets = list(self.buffer.values())
        return ConcatDataset(datasets)

class DistillationLoss(nn.Module):
    def __init__(self, temperature=2.0, alpha=0.7):
        super().__init__()
        self.temperature = temperature
        self.alpha = alpha
        self.ce_loss = nn.CrossEntropyLoss()

    def forward(self, student_logits, teacher_logits, labels):
        ce = self.ce_loss(student_logits, labels)

        if teacher_logits is None:
            return ce

        soft_student = F.log_softmax(student_logits / self.temperature, dim=1)
        soft_teacher = F.softmax(teacher_logits / self.temperature, dim=1)

        kd = F.kl_div(soft_student, soft_teacher, reduction='batchmean')
        kd = kd * (self.temperature ** 2)

        return self.alpha * ce + (1 - self.alpha) * kd

